const express = require('express');
const { check } = require('express-validator');
const userController = require('../controllers/userController');
const { isAuthenticated } = require('../middleware/auth');
const notificationService = require('../services/notificationService');

const router = express.Router();

// Protect all routes in this router
router.use(isAuthenticated);

/**
 * @swagger
 * /users/profile:
 *   put:
 *     summary: Update user profile
 *     tags: [Users]
 *     security:
 *       - bearerAuth: []
 *     requestBody:
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               fullName:
 *                 type: string
 *                 example: "John Doe Smith"
 *               latitude:
 *                 type: number
 *                 example: 40.7128
 *               longitude:
 *                 type: number
 *                 example: -74.0060
 *               preferredLanguage:
 *                 type: string
 *                 enum: [en, es, fr]
 *                 example: "fr"
 *     responses:
 *       200:
 *         description: Profile updated successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 success:
 *                   type: boolean
 *                   example: true
 *                 message:
 *                   type: string
 *                   example: "Profile updated successfully"
 *                 user:
 *                   $ref: '#/components/schemas/User'
 *       400:
 *         description: Validation error
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 *       401:
 *         description: Unauthorized
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 *       404:
 *         description: User not found
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 *       500:
 *         description: Server error
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 */
router.put(
  '/profile',
  [
    check('fullName', 'Full name is required if provided').optional().notEmpty(),
    check('latitude', 'Latitude must be a number').optional().isNumeric(),
    check('longitude', 'Longitude must be a number').optional().isNumeric(),
    check('preferredLanguage', 'Invalid language code').optional().isIn(['en', 'es', 'fr'])
  ],
  userController.updateProfile
);

